# Event Management Portal

## Project Overview

The Event Management Portal is a full-stack web application that enables users to create, manage, and register for events while administrators can organize events, manage participants, and monitor event activities efficiently.

## Features

* User Authentication
* Event Creation and Management
* Event Registration
* Participant Management
* User Dashboard
* Admin Dashboard
* Event Notifications

## Technologies Used

### Frontend

* HTML
* CSS
* JavaScript
* React.js

### Backend

* Node.js
* Express.js

### Database

* MongoDB

## Installation

### Frontend

```bash
cd frontend
npm install
npm start
```

### Backend

```bash
cd backend
npm install
npm start
```

